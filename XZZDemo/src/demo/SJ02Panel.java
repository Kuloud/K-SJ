    @Action
    public void readIntA(){
        JFileChooser fc = new JFileChooser();
        int option = fc.showOpenDialog(getParent());
        if (JFileChooser.APPROVE_OPTION == option) {
            FileReader frA = null;
            try {
                frA = new FileReader(fc.getSelectedFile());
            } catch (FileNotFoundException ex) {
                Logger.getLogger(SJ02Panel.class.getName()).log(Level.SEVERE, null, ex);
            }
            Scanner scanA = new Scanner(frA);
            if(scanA.hasNextBigInteger()) {
                jTextArea1.setText(scanA.nextBigInteger().toString());
            }else {
                jTextArea1.setText("文本错误！请选择正确文件或重新输入。");
                jTextArea1.requestFocus();
                jTextArea1.selectAll();
            }
        }
    }

