    @Action
    public void run() {
        boolean flag = false;
        String strA = jTextArea1.getText().trim();
        String strB = jTextArea2.getText().trim();
        if(strA.charAt(0) == '-') {
            strA = strA.substring(1);
            flag = true;
        }
        if(strB.charAt(0) == '-') {
            strB = strB.substring(1);
            flag = true;
        }
        
        SJ02.strProcess = new StringBuffer("两大整数乘法运算过程：");
        if(flag) {
            SJ02.strProcess.append("\n有负数，略去负号进行运算……");
        }
        try {
            String strResult = SJ02.BigIntMultDif(strA, strB,0);
        } catch (Exception e) {
            jTextArea3.setText("");
            if (errorReport == null) {
                JFrame mainFrame = DemoApp.getApplication().getMainFrame();
                errorReport = new ErrorReport(mainFrame, false);
                errorReport.setLocationRelativeTo(mainFrame);
            }
            DemoApp.getApplication().show(errorReport);
            jTextArea1.requestFocus();
            return;
        }
        
        jTextArea3.setText(SJ02.strProcess.toString());
    }



